#include "3-calc.h"

/**
 * op_add - a addition
 *
 * @a: The operator
 * @b: The second operator
 * Return: (a) (b) Always int (success)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a subtraction
 *
 * @a: The operator
 * @b: The second operator
 * Return: (a) (b) Always int (success)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -  a multiplication
 *
 * @a: The operator
 * @b: The second operator
 * Return: (a) (b) Always int (success)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a division
 *
 * @a: The operator
 * @b: The second operator
 * Return: (a) (b) Always int (success)
 */
int op_div(int a, int b)
{
	
	return (a / b);
}
/**
 * op_mod - a modulo.
 *
 * @a: The operator
 * @b: The second operator
 * Return: (a) (b) Always int (success)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
